package com.cha5.pojo;

public class User {
	
	private String name;
	private StringBuffer url;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public StringBuffer getUrl() {
		return url;
	}
	public void setUrl(StringBuffer url) {
		this.url = url;
	}
	
	
}
